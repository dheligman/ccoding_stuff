#include <cstdio>

int main(){
    printf(" 7 ==  7: %d\n", 7 == 7); // equality
    printf(" 7 !=  7: %d\n", 7 != 7); // inequality
    printf("10 >  20: %d\n", 10 > 20); // greater than
    printf("10 >= 20: %d\n", 10 >= 20); // greater than or equal to
    printf("10 <  20: %d\n", 10 < 20); // less than
    printf("20 <= 20: %d\n", 20 <= 20); // less than or equal to
}